package entitysets;

public class AgeCustom1 {
	String age;
	int number;

	public AgeCustom1(String age, int number) {
		this.age = age;
		this.number = number;
	}

	public String getAge() {
		return age;
	}

	public int getNumber() {
		return number;
	}

	public void setAge(String age) {
		this.age = age;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
}
